    private void btnSearchPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchPaymentActionPerformed
      try {
        String searchText = txtBillCode.getText().trim();
        if (searchText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Bill Code to search.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PaymentsDAO paymentsDAO = new PaymentsDAO();
        List<Payment> payments = paymentsDAO.getAllPayments();
        DefaultTableModel model = (DefaultTableModel) tblPayments.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Payment payment : payments) {
            if (payment.getBillCode().equalsIgnoreCase(searchText)) {
                model.addRow(new Object[]{
                    payment.getPaymentID(),
                    payment.getBillCode(),
                    payment.getPaymentType(),
                    payment.getAmount(),
                    payment.getPaymentDate()
                });
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSearchPaymentActionPerformed
    
