    private void loadPaymentsTable() {
    try {
        PaymentsDAO paymentsDAO = new PaymentsDAO();
        List<Payment> payments = paymentsDAO.getAllPayments();
        DefaultTableModel model = (DefaultTableModel) tblPayments.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Payment payment : payments) {
            model.addRow(new Object[]{
                payment.getPaymentID(),
                payment.getBillCode(),
                payment.getPaymentType(),
                payment.getAmount(),
                payment.getPaymentDate()
            });
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error loading payments: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    private void btnAddPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPaymentActionPerformed
         try {
        String billCode = txtBillCode.getText();
        String paymentType = txtPaymentType.getText();
        BigDecimal amount = new BigDecimal(txtAmount.getText());
        Timestamp paymentDate = Timestamp.valueOf(txtPaymentDate.getText()); // Assuming date is in "YYYY-MM-DD HH:MM:SS" format

        Payment payment = new Payment();
        payment.setBillCode(billCode);
        payment.setPaymentType(paymentType);
        payment.setAmount(amount);
        payment.setPaymentDate(paymentDate);

        PaymentsDAO paymentsDAO = new PaymentsDAO();
        paymentsDAO.addPayment(payment);

        JOptionPane.showMessageDialog(this, "Payment added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        loadPaymentsTable();
        clearFields();
    } catch (SQLException | IllegalArgumentException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnAddPaymentActionPerformed

    private void btnUpdatePaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePaymentActionPerformed
       try {
        int selectedRow = tblPayments.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a payment to update.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int paymentID = Integer.parseInt(txtPaymentID.getText());
        String billCode = txtBillCode.getText();
        String paymentType = txtPaymentType.getText();
        BigDecimal amount = new BigDecimal(txtAmount.getText());
        Timestamp paymentDate = Timestamp.valueOf(txtPaymentDate.getText());

        Payment payment = new Payment();
        payment.setPaymentID(paymentID);
        payment.setBillCode(billCode);
        payment.setPaymentType(paymentType);
        payment.setAmount(amount);
        payment.setPaymentDate(paymentDate);

        PaymentsDAO paymentsDAO = new PaymentsDAO();
        paymentsDAO.updatePayment(payment);

        JOptionPane.showMessageDialog(this, "Payment updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        loadPaymentsTable();
        clearFields();
    } catch (SQLException | IllegalArgumentException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUpdatePaymentActionPerformed

    private void btnDeletePaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletePaymentActionPerformed
        try {
        int selectedRow = tblPayments.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a payment to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int paymentID = Integer.parseInt(txtPaymentID.getText());

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this payment?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            PaymentsDAO paymentsDAO = new PaymentsDAO();
            paymentsDAO.deletePayment(paymentID);

            JOptionPane.showMessageDialog(this, "Payment deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadPaymentsTable();
            clearFields();
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnDeletePaymentActionPerformed

