   private void processReturnItem() {
        try {
            String itemCode = txtItemCode.getText().trim();
            String itemName = txtItemName.getText().trim();
            int returnQuantity = (int) spnQuantity.getValue();
            String returnReason = (String) cmbReturnReason.getSelectedItem();
            String supplier = (String) cmbSupplier.getSelectedItem();

            String stockType = rbtnNew.isSelected() ? "New" : rbtnOld.isSelected() ? "Old" : "";
            if (stockType.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please select a stock type.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (itemCode.isEmpty() || itemName.isEmpty() || returnQuantity <= 0) {
                JOptionPane.showMessageDialog(this, "Please enter valid item details.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ReturnItemDAO returnItemDAO = new ReturnItemDAO();
            ItemDAO itemDAO = new ItemDAO();
            Item item = itemDAO.getByCode(itemCode);
            if (item == null || returnQuantity > item.getQuantity()) {
                JOptionPane.showMessageDialog(this, "Invalid return quantity or item not found.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ReturnItem returnItem = new ReturnItem(itemCode, itemName, returnQuantity, returnReason, supplier, stockType, LocalDate.now());
            if (!returnItemDAO.saveReturnItem(returnItem)) {
                JOptionPane.showMessageDialog(this, "Failed to process return item.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            item.setQuantity(item.getQuantity() - returnQuantity);
            itemDAO.updateItem(item);
            JOptionPane.showMessageDialog(this, "Return processed successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadItemsTable();
            clearFields();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    
   
   public void exportFilteredExcel() {
    try {
        String startDate = txtStartDate.getText().trim();
        String endDate = txtEndDate.getText().trim();
        if (startDate.isEmpty() || endDate.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter both start and end dates.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        List<ReturnItem> returnItems = new ReturnItemDAO().getReturnItemsByDateRange(startDate, endDate);
        if (returnItems.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No return items found in this period.", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setSelectedFile(new File("Return_Report.xlsx"));
        if (fileChooser.showSaveDialog(null) != JFileChooser.APPROVE_OPTION) return;

        File fileToSave = fileChooser.getSelectedFile();
        try (Workbook workbook = new HSSFWorkbook(); FileOutputStream fileOut = new FileOutputStream(fileToSave)) {
            Sheet sheet = workbook.createSheet("Filtered Return Items");
            String[] headers = {"Item Code", "Item Name", "Quantity", "Return Reason", "Supplier", "Stock Type"};
            Row headerRow = sheet.createRow(0);

            for (int i = 0; i < headers.length; i++) {
                Cell cell = headerRow.createCell(i);
                cell.setCellValue(headers[i]);
                CellStyle style = workbook.createCellStyle();
                Font font = workbook.createFont();
                font.setBold(true);
                style.setFont(font);
                cell.setCellStyle(style);
            }

            int rowNum = 1;
            for (ReturnItem item : returnItems) {
                Row row = sheet.createRow(rowNum++);
                row.createCell(0).setCellValue(item.getItemCode());
                row.createCell(1).setCellValue(item.getItemName());
                row.createCell(2).setCellValue(item.getReturnQuantity());
                row.createCell(3).setCellValue(item.getReturnReason());
                row.createCell(4).setCellValue(item.getSupplier());
                row.createCell(5).setCellValue(item.getStockType());
            }

            for (int i = 0; i < headers.length; i++) {
                sheet.autoSizeColumn(i);
            }

            workbook.write(fileOut);
            JOptionPane.showMessageDialog(null, "Excel report generated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (IOException | SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

    
