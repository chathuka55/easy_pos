    private void deleteReturnItem() {
    int selectedRow = tblReturn.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an item to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        String itemCode = (String) tblReturn.getValueAt(selectedRow, 0);
        int returnQuantity = (int) tblReturn.getValueAt(selectedRow, 2);

        ReturnItemDAO returnItemDAO = new ReturnItemDAO();
        ItemDAO itemDAO = new ItemDAO();

        // Step 1: Restore stock in the Items table
        Item item = itemDAO.getByCode(itemCode);
        if (item != null) {
            item.setQuantity(item.getQuantity() + returnQuantity);
            itemDAO.updateItem(item);
        }

        // Step 2: Delete the return item from ReturnItems table
        if (!returnItemDAO.deleteReturnItem(itemCode)) {
            JOptionPane.showMessageDialog(this, "Failed to delete return item.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Remove from UI table
        ((DefaultTableModel) tblReturn.getModel()).removeRow(selectedRow);
        JOptionPane.showMessageDialog(this, "Return item deleted and stock restored.", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}



    private void showItemNameSuggestions() {
        if (allItems == null || allItems.isEmpty()) {
            initializeItemList();
        }

        String input = txtItemName.getText().trim();
        if (input.isEmpty()) return;

        List<Item> filteredItems = allItems.stream()
            .filter(i -> i.getName().toLowerCase().contains(input.toLowerCase()))
            .toList();

        if (filteredItems.isEmpty()) return;

        JPopupMenu suggestions = new JPopupMenu();
        for (Item item : filteredItems) {
            JMenuItem menuItem = new JMenuItem(item.getName() + " - Retail: " + item.getRetailPrice());
            menuItem.addActionListener(evt -> populateItemFields(item));
            suggestions.add(menuItem);
        }
        suggestions.show(txtItemName, 0, txtItemName.getHeight());
    }

    private void populateItemFields(Item item) {
        txtItemName.setText(item.getName());
        txtItemCode.setText(item.getItemCode());
        if (item.isOldStock()) {
            rbtnOld.setSelected(true);
        } else {
            rbtnNew.setSelected(true);
        }
        cmbSupplier.setSelectedItem(item.getSupplierID());
        
    }

    private void loadItemsTable() {
        try {
            ReturnItemDAO returnItemDAO = new ReturnItemDAO();
            List<ReturnItem> returnItems = returnItemDAO.getAll();
            DefaultTableModel model = (DefaultTableModel) tblReturn.getModel();
            model.setRowCount(0);

            for (ReturnItem item : returnItems) {
                model.addRow(new Object[]{
                        item.getItemCode(),
                        item.getItemName(),
                        item.getReturnQuantity(),
                        item.getReturnReason(),
                        item.getSupplier(),
                        item.getStockType()
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading return items: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

